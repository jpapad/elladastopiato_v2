async function main() {
  const payload = await getPayload({ config: configPromise })
  const all = await payload.find({ collection: 'locations', limit: 200, depth: 0 })

  let updated = 0
  for (const loc of all.docs) {
    const correct = CORRECT_NAMES[loc.name as string]
    if (correct && correct !== loc.name) {
      await payload.update({ collection: 'locations', id: loc.id, data: { name: correct } })
      console.log(`  "${loc.name}" → "${correct}"`)
      updated++
    }
  }
  console.log(`\n✓ Fixed accents on ${updated} names.`)
  process.exit(0)
}

main().catch(err => { console.error(err); process.exit(1) })
